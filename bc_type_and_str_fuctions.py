# type() allows to find the data type of a piece of data

ex_1 = False
ex_2 = 29
ex_3 = 4.1352

print(type(ex_1))
print(type(ex_2))
print(type(ex_3))


# str() converts whatever is put between the parenthesis to a string

ex_4 = str(True)
ex_5 = str(5)
ex_6 = str(3.21)

print()
print(type(ex_4))  # notice it changes the type from boolean to string
print(type(ex_5))  # notice it changes the type from int to string
print(type(ex_6))  # notice it changes the type from float to string
