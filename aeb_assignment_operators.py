# Operators that are used for reassignment

add = 5
print(add)
add += 7 # same as Java - this equals to: add_assign(now) = add_assign(before) + 7
print(add)

# same thing happens applies to all the math operators
# substraction
sub = 10
print(sub)
sub -= 5
print(sub)

# multiplication
mult = 10
print(mult)
mult *= 5
print(mult)

# division
div = 10
print (div)
div /= 5
print(div)

# exponentiation
exp = 10
print(exp)
exp **= 2
print(exp)

# floor division
floor = 10
print(floor)
floor //= 6
print(floor)

#modulo
mod = 10
print(mod)
mod %= 7
print(mod)
